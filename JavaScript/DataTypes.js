// String

// Double quote
let name = "John";

// Single Quote
let secondName = "Doe";

// Backtick
let thirdName = `John Doe`;
console.log(typeof name, typeof secondName, typeof thirdName);

// Number

// integer (whole number)
const num = 10;
// float   (decimal)
const floatNum = 10.5;
console.log(typeof num, typeof floatNum);

// Boolean
const gender = false;

console.log(typeof gender);

// Undefined

let typee;

console.log(typeof typee);

// Null

console.log(typeof null);

// Object [array]
const arr = ["John", "Doe", 20, "male"];

const obj = {
  name: "John",
  age: 20,
  gender: "male",
};

console.log(typeof arr, typeof obj);

